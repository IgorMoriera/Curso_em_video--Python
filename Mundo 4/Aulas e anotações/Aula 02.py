# Vantagens da programação Orientada a Objeto

"""

A POO possui algumas caracteristicas e dentre elas podesmos identifcar 6 com o acrônimo: 'COMERNada'

    C - Confiável
        O isolamento entre as partes gera algo mais seguro. Ao alterar uma das partes, nenhuma outra é afetada.
            Ex: Trocar a roda de um carro (seguindo as específicações) não interfere em nada no restante do carro.
                Em outras palavras, não há necessidade de modificar outras 'coisas' no carro.

    O - Oportuno
        Ao divir tudo em partes, cada uma delas pode ser desenvolvida em paralelo.
            Ex: Se produz o estofado, painel e demais peças (módulos) em paralelo para no final juntar tudo (main code).

    M - Manutenível
        Atualizar é mais fácil. Uma pequena alteração vai beneficiar todas as partes relacionadas.
            Ex: Qualquer melhoria, como a troca de um motor fraco para um mais potente beneficia as demais partes do
                carro entregando mais potência.

    E - Extensível
        Um sistema não deve ser estático. Tudo deve mudar e crescer para permanecer útil.
            Ex: Extender a capacidade de carregar mais itens no carro, como adicoinar uma carretinha.
                É mais fácil do que trocar o carro por outro com mais espaço.

    R - Reutilizável
        Objetos que foram criados para um sistema podem ser aproveitados em outros sistemas.
            Ex: Não precisa projetar diferentes motores para cada modelo de carro, reutilizamos o mesmo motor para
                diferentes modelos.

    N - Natural
        Mais fácil de entender. Maior atenção às funcionalidades do que aos detalhes de implementação.
            Ex: Você não precisa saber como cada sitema ou peça funciona em edtalhes (como usuário), basta saber o
                básico para tomadas de decisão.

"""